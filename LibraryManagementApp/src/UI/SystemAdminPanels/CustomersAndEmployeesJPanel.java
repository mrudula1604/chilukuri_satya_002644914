    public void displayCustomers(){
        ArrayList<Customer> customers = this.business.getCustomersList().getCustomerlist();
        if(customers.size() >= 0) {
            customersTableModel.setRowCount(0);
            for(Customer c: customers) {
                Object row[] = new Object[2];
                row[0] = c.getPersonID();
                row[1] = c.getName();
                customersTableModel.addRow(row);
            }
        }
    }
    
    public void populateBranches()
    {
        ArrayList<Branch> branches = this.business.getBranches().getBranchesList();
        
        for(Branch b: branches) {
            selectBranchCBox.addItem(b.getBranchId());
        }
    }
    
    public void displayEmployees(){
        ArrayList<Employee> employees = this.business.getEmployeesList().getEmployees();
        if(employees.size() >= 0) {
            EmployeesTableModel.setRowCount(0);
            for(Employee e: employees) {
                Object row[] = new Object[5];
                row[0] = e.getPersonID();
                row[1] = e.getName();
                row[2] = e.getDesignation();
                row[3] = e.getYearsOfExperience();
                EmployeesTableModel.addRow(row);
            }
        }
    }

